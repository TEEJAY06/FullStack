var pi = 3.14317;

function pi2() {
  return pi * 2;
}

function pi3() {
  return pi * 3;
}

function add(n1, n2) {
  return n1 + n2;
}

function multiply(n1, n2) {
  return n1 * n2;
}

function subtract(n1, n2) {
  return n1 - n2;
}

function divide(n1, n2) {
  return n1 / n2;
}

export default pi;
export { pi2, pi3, add, multiply, divide, subtract };
